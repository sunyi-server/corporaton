//添加模块
function overlay1(){
	var oUserChange=document.getElementById("modal_userchange");
	oUserChange.style.visibility=(oUserChange.style.visibility=="visible") ? "hidden":"visible";
}

function hide1(){
	var oUserChange=document.getElementById("modal_userchange");
	oUserChange.style.visibility="hidden";
}

